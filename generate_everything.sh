set -e

MD2HDR="`pwd`/fmu-builder/modeldescription2header.py"
CMAKE_GENERATOR="`pwd`/fmu-builder/cmake-generator.py -t `pwd`/templates/fmi2/ -i `pwd`/../FMILibrary-2.0.1/ThirdParty/FMI/default -m ${MD2HDR}"

WRAPPERSOURCES="`pwd`/fmu-builder/sources/"
XML2WRAPPEDXML="`pwd`/fmu-builder/xml2wrappedxml.py"

GSLFMUS="
    gsl2/clutch2
    gsl2/clutch3
    gsl2/chained_sho
    gsl2/exp
    gsl2/clutch_ef
    gsl2/clutch
    gsl2/coupled_sho
    gsl2/mass_force
    gsl2/mass_force_fe
    gsl2/trailer
    gsl2/engine2
"

FMUS="
    impulse
    lumpedrod
    kinematictruck/body
    kinematictruck/engine
    kinematictruck/gearbox2
    kinematictruck/kinclutch
    forcevelocitytruck/fvbody
    forcevelocitytruck/gearbox
    testfmus/typeconvtest
    testfmus/loopsolvetest/add
    testfmus/loopsolvetest/sub
    testfmus/loopsolvetest/mul
    testfmus/stringtest
    me/springs
    me/springs2
    me/bouncingBall
    me/bouncingBallWithSpring
    me/fixedPoint
"

WRAPPERS="
    bouncingBall
    springs
    springs2
"

# The meat of CMakeLists.txt is kept in FmuBase.cmake for smaller diffs
cat <<END>CMakeLists.txt
#this file is generated by generate_everything. DO NOT EDIT!
cmake_minimum_required(VERSION 2.8)
include(FmuBase.cmake)

END

# Warnings during compilation may go unnoticed without -Werror, leading to
# hard-to-debug problems
export CFLAGS="-Wall -Werror -O3"

#New GSL interface
for d in $GSLFMUS
do
    echo "add_subdirectory($d)" >> CMakeLists.txt
    # TODO: figure out how to include cgsl into each FMU as-is
    # We don't have any CMakeLists.txt yet anyway
    GSL="-l cgsl,m -c"
    pushd $d
        python ${MD2HDR} modelDescription.xml > sources/modelDescription.h
        python ${CMAKE_GENERATOR} ${GSL}
    popd
done

# Co-Simulation and Mode Exchange FMUs
for d in $FMUS
do
    echo "add_subdirectory($d)" >> CMakeLists.txt
    pushd $d
        python ${MD2HDR} modelDescription.xml > sources/modelDescription.h
        python ${CMAKE_GENERATOR}
    popd
done

# Wrappers
for d in $WRAPPERS
do
    echo "add_subdirectory(meWrapper/$d)" >> CMakeLists.txt
    GSL="-l cgsl,wrapperlib,m,fmilib"
    mkdir -p meWrapper/${d}
    SRCXML=$(pwd)/me/${d}/modelDescription.xml
    DSTXML=$(pwd)/meWrapper/${d}/modelDescription.xml
    pushd meWrapper/$d
        mkdir -p sources
        python ${XML2WRAPPEDXML} -x ${SRCXML} > ${DSTXML}
        python ${MD2HDR} -w ${DSTXML} > sources/modelDescription.h
        python ${CMAKE_GENERATOR} ${GSL} -t ${WRAPPERSOURCES} -s ${SRCXML} -x ${XML2WRAPPEDXML}
    popd
done
